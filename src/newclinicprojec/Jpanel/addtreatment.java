    private void btndeletetreatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeletetreatActionPerformed
        // TODO add your handling code here:
         int i=tableTreatmentDetail.getSelectedRow();
        DetailTreatment n=(DetailTreatment) detailTreamentTableModel.getDetailtreatment().get(i);
        try {
            if(n.getId()!=null){
            treatmentController.destroy(n.getId());
            }
            detailTreamentTableModel.getDetailtreatment().remove(i);
            detailTreamentTableModel.fireTableDataChanged();
        } catch (Exception ex){
        Logger.getLogger(addtreatment.class.getName()).log(Level.SEVERE, null, ex);
        }
        setNullFiel();
    }//GEN-LAST:event_btndeletetreatActionPerformed

