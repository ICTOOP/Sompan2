    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        int treatmentid = Integer.parseInt(txttreatmentid.getText());
        setTreatment.setTreatmentId(treatmentid);
        int customid = Integer.parseInt(txtcustomerid.getText());
        setTreatment.setCustomerId(customid);

        setTreatment.setDoctorId(Integer.parseInt(txtdoctorid.getText()));

        setTreatment.setDate(txtdate.getText());

        setTreatment.setDetailTreatments(detailTreamentTableModel.getDetailtreatment());
        Double totalcost = setTreatment.calculateCost();
        setTreatment.setTotalCost(totalcost);

        if (setTreatment.getId() != null && setTreatment.getId() != 0) {
            try {
                treatmentController.save(setTreatment);

            } catch (Exception ex) {
                Logger.getLogger(addtreatment.class
                        .getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            try {
                treatmentController.save(setTreatment);
            } catch (Exception ex) {
                Logger.getLogger(addtreatment.class.getName()).log(Level.SEVERE, null, ex);
            }
            // int ii=1;
            treatmentTablemodel.getTreatment().add(setTreatment);

            setFielNull();
            setNullFiel();
            detailTreamentTableModel.getDetailtreatment().clear();
            detailTreamentTableModel.fireTableDataChanged();
        }
        setEnableInputForm(inputform, false);
        treatmentTablemodel.fireTableDataChanged();

    }//GEN-LAST:event_btnsaveActionPerformed
