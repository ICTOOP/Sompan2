    private void bnteditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnteditActionPerformed
        // TODO add your handling code here:
        int i=tablecustomerdata.getSelectedRow();
       // System.out.println("HELLO");
        Customer customer = customerTableModel.customer.get(i);
        String cusid=customer.getCustomerid().toString();
        String sex=customer.getSex();
        txtcustomerid.setText(cusid);
        txtname.setText(customer.getName());
        if(sex=="Male")
            gender="Male";
        else
            gender="Female";
        Enumeration buttons = buttonGroup1.getElements();
        while(buttons.hasMoreElements()){
            AbstractButton btn = (AbstractButton)buttons.nextElement();
            if (btn.getActionCommand().equalsIgnoreCase(sex)){
                btn.setSelected(true);
            }
        }
        
        
        txtbirthdate.setText(customer.getBirthdate());
        txtaddress.setText(customer.getAddress());
        txtphone.setText(customer.getPhone());
        txtdisease.setText(customer.getCongenital());
        txtdrug.setText(customer.getDrug());
        List<Customer> customers=customerTableModel.getCustomer();
        setEnableCustomerdata(customerdata, true);
        txtcustomerid.setEnabled(false);
        currentCustomer=customer;
       
    }//GEN-LAST:event_bnteditActionPerformed

