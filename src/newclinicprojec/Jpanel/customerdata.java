    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //get set model
        String cust=txtcustomerid.getText();
        int cumid=Integer.parseInt(cust);
        currentCustomer.setCustomerid(cumid);
        currentCustomer.setName(txtname.getText());
        currentCustomer.setSex(gender);
        currentCustomer.setBirthdate(txtbirthdate.getText());
        currentCustomer.setAddress(txtaddress.getText());
        currentCustomer.setPhone(txtphone.getText());
        currentCustomer.setCongenital(txtdisease.getText());
        currentCustomer.setDrug(txtdrug.getText());
        if(currentCustomer.getId() != null&&currentCustomer.getId()!=0){
            try {
                controller.edit(currentCustomer);
                customerTableModel.fireTableDataChanged();
            } catch (Exception ex) {
                Logger.getLogger(customerdata.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else {
            controller.create(currentCustomer);
           // int ii=1;
           customerTableModel.customer.add(currentCustomer);
            customerTableModel.fireTableDataChanged();
        }
        setNullFiel();
        this.setEnableCustomerdata(customerdata,false);
    }//GEN-LAST:event_jButton1ActionPerformed

